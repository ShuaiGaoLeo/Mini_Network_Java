    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        String status = this.status.getText().trim();
        String fname = this.fname.getText().trim();
        String lname = this.lname.getText().trim();

        if (new MethodImp().checkInt(this.age.getText().trim()) && new MethodImp().checkStr(this.fname.getText().trim())
                && new MethodImp().checkStr(this.lname.getText().trim())) {
            boolean addp = false;
int age = Integer.parseInt(this.age.getText().trim());
            String gender = this.cbGender.getSelectedItem().toString();
            int sex = 3;
            if (gender.equals("Male")) {
                sex = 1;
            } else if (gender.equals("Female")) {
                sex = 0;
            }
            boolean picture = Boolean.parseBoolean(this.cbPicture.getSelectedItem().toString().trim());
            if (age >= 16) {
                addp = new AddPerson().addAdult(fname, lname, sex, age, status, picture);
            } else {
                JOptionPane.showMessageDialog(this, "A Child must has parents");
                
                
                String father=null;
                String mother=null;
               try{
                 father = JOptionPane.showInputDialog(this, "Enter father's name", "Father", 2).trim();
                 mother = JOptionPane.showInputDialog(this, "Enter mother's name", "mother", 2).trim();
               }catch(NullPointerException e){

               }
           if(father==null||mother==null){
               return;
           }else{
              try{
            	  	
                      addp = new AddPerson().addChild(fname, lname, sex, age, status, picture, father, mother);}
              catch(NullPointerException e){
                  new MyExceptions("Don't have these people!");
              }
                
           }
                
            }

            if (addp) {
                JOptionPane.showMessageDialog(this, "Add Successful!");
                if (Output.fileAccessPassed){
                	Output.outPeople(new Output().getFile());//output to txt
                	Output.outRelation(new Output().getFileRe());
                }
                else {
                	DatabaseHandler.writePeople();
                	DatabaseHandler.writeRelationship(); 
                }
                this.fname.setText("");
                this.lname.setText("");
                this.age.setText("");
                this.status.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Error!!! Try again!");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Input error! Try to input correctly!");
        }


    }//GEN-LAST:event_jButton1ActionPerformed


